      // –°–æ–±–∏—Ä–∞–µ–º –∫–æ–Ω—Ç–µ–Ω—Ç –±–µ–π–¥–∂–∞ —Å –º–∏–Ω–∏–∞—Ç—é—Ä–æ–π –∏ ID
      const stickerContent = `<span class="d-inline-flex align-items-center gap-2">${thumbHtml}<span>${stickerLabel}</span></span>`; // –†–∞–∑–º–µ—Ç–∫–∞ –±–µ–π–¥–∂–∞ —Å—Ç–∏–∫–µ—Ä–∞
