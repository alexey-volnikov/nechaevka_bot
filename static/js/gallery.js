    if (!hasGalleryDom) {
      return; // –í—ã—Ö–æ–¥–∏–º, –µ—Å–ª–∏ –º–æ–¥–∞–ª–∫–∏ –Ω–µ—Ç
    } // –ó–∞–≤–µ—Ä—à–∞–µ–º –ø—Ä–æ–≤–µ—Ä–∫—É –Ω–∞–ª–∏—á–∏—è –º–æ–¥–∞–ª–∫–∏
