    const buttons = galleryData.items.map((item, idx) => {
      // –û–ø—Ä–µ–¥–µ–ª—è–µ–º –ø–æ–¥–ø–∏—Å—å —Ç–∏–ø–∞ –¥–ª—è –∫–Ω–æ–ø–∫–∏
      const typeLabel = buildTypeLabel(item.type || 'file'); // –ü–æ–¥–ø–∏—Å—å —Ç–∏–ø–∞ –≤–ª–æ–∂–µ–Ω–∏—è —Å —ç–º–æ–¥–∑–∏
      // –û–ø—Ä–µ–¥–µ–ª—è–µ–º –∞–∫—Ç–∏–≤–Ω—ã–π –∫–ª–∞—Å—Å –¥–ª—è —Ç–µ–∫—É—â–µ–≥–æ —ç–ª–µ–º–µ–Ω—Ç–∞
      const activeClass = idx === currentGalleryIndex ? 'active' : ''; // –ê–∫—Ç–∏–≤–Ω–∞—è –∫–Ω–æ–ø–∫–∞
      return `<button type="button" class="btn btn-outline-light btn-sm gallery-thumb ${activeClass}" data-gallery-index="${idx}">${idx + 1}. ${typeLabel}</button>`; // –í–æ–∑–≤—Ä–∞—â–∞–µ–º HTML –∫–Ω–æ–ø–∫–∏
    }); // –ó–∞–≤–µ—Ä—à–∞–µ–º –≥–µ–Ω–µ—Ä–∞—Ü–∏—é –∫–Ω–æ–ø–æ–∫
