    const failedReasonsText = failedReasons.size ? Array.from(failedReasons).join('; ') : '–ü—Ä–∏—á–∏–Ω–∞ –Ω–µ —É–∫–∞–∑–∞–Ω–∞'; // –ö–æ–Ω—Å—Ç—Ä—É–∏—Ä—É–µ–º —Å—Ç—Ä–æ–∫—É –ø–æ—è—Å–Ω–µ–Ω–∏—è
    // –§–æ—Ä–º–∏—Ä—É–µ–º –±–µ–π–¥–∂ –∑–∞–≥—Ä—É–∑–∫–∏ –ø—Ä–∏ –Ω–∞–ª–∏—á–∏–∏ –æ–∂–∏–¥–∞–Ω–∏–π
    const pendingBadge = pendingTotal
      ? `<span class="attachment-pill attachment-pending" data-gallery-key="${galleryKey}">‚è≥ –í–ª–æ–∂–µ–Ω–∏—è —Å–æ—Ö—Ä–∞–Ω—è—é—Ç—Å—è (${pendingTotal})</span>`
      : ''; // –ì–æ—Ç–æ–≤–∏–º –±–µ–π–¥–∂ –æ–∂–∏–¥–∞–Ω–∏—è –∏–ª–∏ –ø—É—Å—Ç—É—é —Å—Ç—Ä–æ–∫—É
    // –§–æ—Ä–º–∏—Ä—É–µ–º –±–µ–π–¥–∂ –æ—à–∏–±–æ–∫ –ø—Ä–∏ –Ω–∞–ª–∏—á–∏–∏ –Ω–µ—É–¥–∞—á–Ω—ã—Ö –∑–∞–≥—Ä—É–∑–æ–∫
    const failedBadge = failedTotal
      ? `<span class="attachment-pill attachment-failed" data-gallery-key="${galleryKey}">‚ö†Ô∏è –í–ª–æ–∂–µ–Ω–∏—è –Ω–µ —Å–æ—Ö—Ä–∞–Ω–∏–ª–∏—Å—å (${failedTotal}) ‚Äî ${failedReasonsText}</span>`
      : ''; // –ì–æ—Ç–æ–≤–∏–º –±–µ–π–¥–∂ –æ—à–∏–±–∫–∏ –∏–ª–∏ –ø—É—Å—Ç—É—é —Å—Ç—Ä–æ–∫—É
    // –°—Ç—Ä–æ–∏–º –±–µ–π–¥–∂ —Å–æ —Å–∫—Ä–µ–ø–∫–æ–π –∏ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ–º
    const summary = buildAttachmentSummary(regularAttachments, copyHistory, galleryKey); // HTML —Å–∫—Ä–µ–ø–∫–∏ –±–µ–∑ —Å—Ç–∏–∫–µ—Ä–æ–≤
