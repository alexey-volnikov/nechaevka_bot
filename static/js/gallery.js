    galleryMedia.innerHTML = mediaHtml; // –†–µ–Ω–¥–µ—Ä–∏–º –º–µ–¥–∏–∞
    // –§–æ—Ä–º–∏—Ä—É–µ–º –ø–æ–¥–ø–∏—Å—å —Ñ–∞–π–ª–∞
    const caption = item.caption || '–ë–µ–∑ –æ–ø–∏—Å–∞–Ω–∏—è'; // –ü–æ–¥–ø–∏—Å—å —Ñ–∞–π–ª–∞
    // –û–ø—Ä–µ–¥–µ–ª—è–µ–º –∏—Å—Ç–æ—á–Ω–∏–∫ —Ñ–∞–π–ª–∞
