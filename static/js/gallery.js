  // –°—á–∏—Ç–∞–µ–º –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –≤–ª–æ–∂–µ–Ω–∏–π –ø–æ —Ç–∏–ø–∞–º –≤ –æ–±—ã—á–Ω–æ–º —Å–ø–∏—Å–∫–µ
