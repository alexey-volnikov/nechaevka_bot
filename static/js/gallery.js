    const stickerAttachments = safeAttachments.filter((att) => att && att.type === 'sticker'); // –í—ã–¥–µ–ª—è–µ–º —Å—Ç–∏–∫–µ—Ä—ã
    const regularAttachments = safeAttachments.filter((att) => !att || att.type !== 'sticker'); // –û—Å—Ç–∞–ª—å–Ω—ã–µ –≤–ª–æ–∂–µ–Ω–∏—è
    // –°—á–∏—Ç–∞–µ–º –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –≤–ª–æ–∂–µ–Ω–∏–π, –∫–æ—Ç–æ—Ä—ã–µ –µ—â–µ –Ω–µ –≥–æ—Ç–æ–≤—ã
    const pendingTotal = countPendingAttachments(attachments) + countPendingCopyHistory(copyHistory); // –û–∂–∏–¥–∞—é—â–∏–µ –≤–ª–æ–∂–µ–Ω–∏—è
    // –°—á–∏—Ç–∞–µ–º –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –≤–ª–æ–∂–µ–Ω–∏–π, –∫–æ—Ç–æ—Ä—ã–µ –Ω–µ —É–¥–∞–ª–æ—Å—å —Å–æ—Ö—Ä–∞–Ω–∏—Ç—å
    const failedTotal = countFailedAttachments(attachments) + countFailedCopyHistory(copyHistory); // –û—à–∏–±–∫–∏ –∑–∞–≥—Ä—É–∑–∫–∏
    // –°–æ–±–∏—Ä–∞–µ–º –ø–æ—è—Å–Ω–µ–Ω–∏—è, –ø–æ—á–µ–º—É –∑–∞–≥—Ä—É–∑–∫–∏ –∑–∞–≤–µ—Ä—à–∏–ª–∏—Å—å –æ—à–∏–±–∫–æ–π
    const failedReasons = new Set(); // –ü–æ–¥–≥–æ—Ç–∞–≤–ª–∏–≤–∞–µ–º –º–Ω–æ–∂–µ—Å—Ç–≤–æ —É–Ω–∏–∫–∞–ª—å–Ω—ã—Ö –ø—Ä–∏—á–∏–Ω
    collectFailedReasons(attachments, failedReasons); // –î–æ–±–∞–≤–ª—è–µ–º –ø—Ä–∏—á–∏–Ω—ã –æ—à–∏–±–æ–∫ –∏–∑ –≤–ª–æ–∂–µ–Ω–∏–π —Å–æ–æ–±—â–µ–Ω–∏—è
    collectFailedReasonsCopyHistory(copyHistory, failedReasons); // –î–æ–±–∞–≤–ª—è–µ–º –ø—Ä–∏—á–∏–Ω—ã –∏–∑ –≤–ª–æ–∂–µ–Ω–Ω—ã—Ö —Ä–µ–ø–æ—Å—Ç–æ–≤
