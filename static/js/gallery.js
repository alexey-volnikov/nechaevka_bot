    const copyCount = countCopyHistoryAttachments(copyHistory); // –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ –≤–ª–æ–∂–µ–Ω–∏–π copy_history
    // –ü–æ–¥—Å—á–∏—Ç—ã–≤–∞–µ–º –∏—Ç–æ–≥–æ–≤–æ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –≤–ª–æ–∂–µ–Ω–∏–π
    const total = attachmentsCount + copyCount; // –û–±—â–µ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –≤–ª–æ–∂–µ–Ω–∏–π
    // –ï—Å–ª–∏ –≤–ª–æ–∂–µ–Ω–∏–π –Ω–µ—Ç, –≤–æ–∑–≤—Ä–∞—â–∞–µ–º –ø–ª–µ–π—Å—Ö–æ–ª–¥–µ—Ä
    if (!total) {
      return '<span class="text-secondary">‚Äî</span>'; // –í–æ–∑–≤—Ä–∞—â–∞–µ–º —Ç–∏—Ä–µ
    } // –ó–∞–≤–µ—Ä—à–∞–µ–º –ø—Ä–æ–≤–µ—Ä–∫—É –æ—Ç—Å—É—Ç—Å—Ç–≤–∏—è –≤–ª–æ–∂–µ–Ω–∏–π
    // –ò—â–µ–º —Å—Å—ã–ª–∫—É –ø–µ—Ä–≤–æ–≥–æ –≤–ª–æ–∂–µ–Ω–∏—è
    const previewLink = findFirstAttachmentLink(attachments) || findFirstCopyHistoryLink(copyHistory); // –°—Å—ã–ª–∫–∞ –¥–ª—è –ø—Ä–µ–¥–ø—Ä–æ—Å–º–æ—Ç—Ä–∞
    // –ü–æ–¥–±–∏—Ä–∞–µ–º CSS-–∫–ª–∞—Å—Å –¥–ª—è –±–µ–π–¥–∂–∞
    const className = pickAttachmentClass(total === 1 ? (attachments?.[0]?.type || 'file') : 'file'); // –ö–ª–∞—Å—Å –±–µ–π–¥–∂–∞
    // –°—á–∏—Ç–∞–µ–º –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –≤–ª–æ–∂–µ–Ω–∏–π –∫–∞–∂–¥–æ–≥–æ —Ç–∏–ø–∞
    const typeCounters = {}; // –•—Ä–∞–Ω–∏–ª–∏—â–µ —Å—á—ë—Ç—á–∏–∫–æ–≤ —Ç–∏–ø–æ–≤
    countTypesInList(attachments, typeCounters); // –î–æ–±–∞–≤–ª—è–µ–º —Ç–∏–ø—ã –∏–∑ –æ—Å–Ω–æ–≤–Ω–æ–≥–æ —Å–æ–æ–±—â–µ–Ω–∏—è
    countTypesInCopyHistory(copyHistory, typeCounters); // –î–æ–±–∞–≤–ª—è–µ–º —Ç–∏–ø—ã –∏–∑ —Ä–µ–ø–æ—Å—Ç–æ–≤
    // –§–æ—Ä–º–∏—Ä—É–µ–º —á–µ–ª–æ–≤–µ–∫–æ—á–∏—Ç–∞–µ–º–æ–µ –æ–ø–∏—Å–∞–Ω–∏–µ —Ç–∏–ø–æ–≤ —Å —ç–º–æ–¥–∑–∏
    const typeLabels = Object.entries(typeCounters).map(([key, value]) => {
      // –ü—Ä–æ–ø—É—Å–∫–∞–µ–º —Å—Ç–∏–∫–µ—Ä—ã, —á—Ç–æ–±—ã –Ω–µ –¥—É–±–ª–∏—Ä–æ–≤–∞—Ç—å –ø–æ–¥–ø–∏—Å—å –ø—Ä–∏ –æ—Ç–¥–µ–ª—å–Ω–æ–º –ø—Ä–µ–≤—å—é
      if (key === 'sticker') { // –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ —Ç–∏–ø ‚Äî —Å—Ç–∏–∫–µ—Ä
        return null; // –í–æ–∑–≤—Ä–∞—â–∞–µ–º null, —á—Ç–æ–±—ã —É–±—Ä–∞—Ç—å —Ç–∏–ø –∏–∑ –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è
      } // –ö–æ–Ω–µ—Ü –ø—Ä–æ–≤–µ—Ä–∫–∏ —Ç–∏–ø–∞
      const label = buildTypeLabel(key); // –ü–æ–ª—É—á–∞–µ–º –ø–æ–¥–ø–∏—Å—å —Å —ç–º–æ–¥–∑–∏ –¥–ª—è —Ç–∏–ø–∞
      return value > 1 ? `${label} √ó${value}` : label; // –î–æ–±–∞–≤–ª—è–µ–º –º–Ω–æ–∂–∏—Ç–µ–ª—å –ø—Ä–∏ –Ω–µ–æ–±—Ö–æ–¥–∏–º–æ—Å—Ç–∏
    }); // –ó–∞–≤–µ—Ä—à–∞–µ–º —Å–±–æ—Ä–∫—É –æ–ø–∏—Å–∞–Ω–∏–π —Ç–∏–ø–æ–≤
    // –§–æ—Ä–º–∏—Ä—É–µ–º —Ç–µ–∫—Å—Ç –±–µ–π–¥–∂–∞ —Å —Ç–∏–ø–∞–º–∏ –∏ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ–º
    const filteredLabels = typeLabels.filter((value) => Boolean(value)); // –£–±–∏—Ä–∞–µ–º –ø—Ä–æ–ø—É—â–µ–Ω–Ω—ã–µ —Ç–∏–ø—ã (—Å—Ç–∏–∫–µ—Ä—ã)
    const label = filteredLabels.length ? filteredLabels.join(' ‚Ä¢ ') : `üìé ${total}`; // –¢–µ–∫—Å—Ç —Å–∫—Ä–µ–ø–∫–∏ —Å –ø–µ—Ä–µ—á–Ω–µ–º —Ç–∏–ø–æ–≤
