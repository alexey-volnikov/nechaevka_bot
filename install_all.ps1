# Проверяем, создано ли виртуальное окружение в папке .venv
if (-not (Test-Path ".venv")) {  # Условие срабатывает, если каталога .venv нет
    python -m venv .venv  # Создаем новое виртуальное окружение
}  # Закрываем условный блок создания окружения

# Активируем виртуальное окружение для текущего окна PowerShell
. ".\\.venv\\Scripts\\Activate.ps1"  # Подключаем скрипт активации окружения

# Обновляем pip внутри окружения до актуальной версии
python -m pip install --upgrade pip  # Ставим свежую версию менеджера пакетов

# Устанавливаем все зависимости проекта одной командой
pip install -r requirements.txt  # Читаем список модулей из requirements.txt и ставим их

# Сообщаем об успешном завершении установки
Write-Host "Все зависимости установлены. Можно запускать python app.py"  # Печатаем подсказку в терминал
